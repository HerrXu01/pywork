#在程序中可以随时修改变量的值，python始终记录变量的最新值
message = "Hello Python Crash Course world!"
print(message)

#变量的命名和使用
#变量名只能包含字母、数字、下划线，且不能以数字打头
#变量名不能包含空格，只能用下划线分隔开
#不能将python关键字和函数名用作变量名
#变量名应该简短且具有描述性
#变量名慎用小写字母l和大写字母O，可能会被误认成数字1和0
#变量名尽量避免使用大写字母
